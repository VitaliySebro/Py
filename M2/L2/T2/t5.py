# Запитайте користувача ввести число
n = int(input("Введіть число: "))

# Ініціалізуйте змінну для зберігання суми
total = 0

# Просумуйте числа від 1 до n
for i in range(1, n + 1):
    total += i

# Виведіть результат
print(f"Сума чисел від 1 до {n} дорівнює {total}")
