'''
Програма повинна запросити два числа, 
відняти друге від першого і надрукувати результат.
Друкується тільки отримане число.
'''
# Запитайте користувача на введення першого числа
num1 = float(input("Введіть перше число: "))

# Запитайте користувача на введення другого числа
num2 = float(input("Введіть друге число: "))

# Відніміть друге число від першого і виведіть результат
result = num1 - num2
print(result)
